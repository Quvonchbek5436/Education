export const statisticsData = regions.map(({ id, regionName }) => {
    let k = 0;
    educations.forEach(({ regId }) => {
        if (regId === id)
            k++
    })
    return { regionName: regionName, Markazlar: k }
})

console.log(statisticsData);

statisticsData.sort(function (a, b) {
    if (b.Markazlar > a.Markazlar) {
        return 1;
    }
    if (b.Markazlar < a.Markazlar) {
        return -1;
    }
    // a должно быть равным b
    return 0;
});